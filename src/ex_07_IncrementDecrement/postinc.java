package ex_07_IncrementDecrement;

public class postinc {
    public static void main(String[] args) {

        int a=10;
        System.out.println(a++);
        System.out.println(a);



        int x=10;
        int result=++x;
        System.out.println(x);
        System.out.println(result);


    }
}
