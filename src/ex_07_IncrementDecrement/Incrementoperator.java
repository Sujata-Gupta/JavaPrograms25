package ex_07_IncrementDecrement;

public class Incrementoperator {
    public static void main(String[] args) {
        int a=10;
        int b=++a;
        System.out.println(a);
        System.out.println(b);


        int x=10;
        System.out.println(++x);
        System.out.println(x);







    }
}
